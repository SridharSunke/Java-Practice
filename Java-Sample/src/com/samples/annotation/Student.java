package com.samples.annotation;

public class Student {

	
	@JsonField
	String id;
	
	@JsonField
	String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
